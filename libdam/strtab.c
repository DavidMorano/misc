/* make an index table of the string table */
int strtab_indmk(STRTAB *op,int (*it)[3],int itsize,int nskip)
{
	VECOBJ		ses ;
	const int	esize = sizeof(STRENTRY) ;
	int		rs ;
	int		rs1 ;
	int		il ;
	int		isize ;
	int		opts ;
	int		sc = 0 ;

#if	CF_SAFE1
	if (op == NULL) return SR_FAULT ;
#endif

#if	CF_SAFE2
	if (op->magic != STRTAB_MAGIC) return SR_NOTOPEN ;
#endif

	if (nskip < 0)
	    nskip = 0 ;

	il = indexlen(op->count + 1) ;

	isize = indexsize(il) ;

	if (itsize < isize)
	    return SR_OVERFLOW ;

/* start in */

	memset(it,0,isize) ;

	opts = VECOBJ_OCOMPACT ;
	if ((rs = vecobj_start(&ses,esize,op->count,opts)) >= 0) {
	    STRENTRY	se, *sep ;
	    HDB_CUR	cur ;
	    HDB_DATUM	key, val ;
	    uint	khash, chash, nhash ;
	    int		lhi, nhi, hi, si ;

	    if ((rs = hdb_curbegin(&op->strdb,&cur)) >= 0) {
		int		*ip ;
		int		sl ;
		const char	*sp ;

	        while ((sl = hdb_enum(&op->strdb,&cur,&key,&val)) >= 0) {

	            sp = (const char *) key.buf ;
	            sl = key.len ;

	            ip = (int *) val.buf ;
	            si = *ip ;
	            khash = hashelf(sp,sl) ;

	            hi = hashindex(khash,il) ;

	            if (it[hi][0] == 0) {

	                it[hi][0] = si ;
	                it[hi][1] = (khash & INT_MAX) ;
	                it[hi][2] = 0 ;
	                sc += 1 ;

	            } else {

	                se.khash = khash ;
	                se.si = si ;
	                se.hi = hi ;
	                rs = vecobj_add(&ses,&se) ;

	            } /* end if */

	            if (rs < 0) break ;
	        } /* end while (looping through strings) */

	        hdb_curend(&op->strdb,&cur) ;
	    } /* end if (cursor) */

	    if (rs >= 0) {
		int	i ;
		int	c ;

	        for (i = 0 ; vecobj_get(&ses,i,&sep) >= 0 ; i += 1) {

	            khash = sep->khash ;
	            si = sep->si ;
	            hi = sep->hi ;

	            chash = (khash & INT_MAX) ;
	            nhash = khash ;

	            c = 0 ;
	            while (it[hi][0] > 0) {

	                if ((it[hi][1] & INT_MAX) == chash)
	                    break ;

	                it[hi][1] |= (~ INT_MAX) ;
	                nhash = hashagain(nhash,c,nskip) ;

	                hi = hashindex(nhash,il) ;

	                c += 1 ;

	            } /* end while */

	            sc += c ;
	            if (it[hi][0] > 0) {

	                lhi = hi ;
	                while ((nhi = it[lhi][2]) > 0)
	                    lhi = nhi ;

	                hi = hashindex((lhi + 1),il) ;

	                while (it[hi][0] > 0)
	                    hi = hashindex((hi + 1),il) ;

	                it[lhi][2] = hi ;

	            } /* end while */

	            it[hi][0] = si ;
	            it[hi][1] = chash ;
	            it[hi][2] = 0 ;

	        } /* end for */

	        it[il][0] = -1 ;
	        it[il][1] = 0 ;
	        it[il][2] = 0 ;

	        if (sc < 0)
	            sc = 0 ;

	    } /* end if */

	    rs1 = vecobj_finish(&ses) ;
	    if (rs >= 0) rs = rs1 ;
	} /* end if (vecobj) */

	return (rs >= 0) ? sc : rs ;
}
/* end subroutine (strtab_indmk) */


