/* make the record table */
int strtab_recmk(STRTAB *op,int *rec,int recsize)
{
	int		rs ;
	int		n, size ;
	int		c = 0 ;

#if	CF_SAFE1
	if (op == NULL) return SR_FAULT ;
#endif

#if	CF_SAFE2
	if (op->magic != STRTAB_MAGIC) return SR_NOTOPEN ;
#endif

	n = (op->count + 1) ;
	size = (n + 1) * sizeof(int) ;
	if (recsize >= size) {
	    HDB_CUR	cur ;
	    HDB_DATUM	key, val ;
	    int	*ip ;
	    rec[c++] = 0 ;			/* ZERO-entry is NUL-string */
	    if ((rs = hdb_curbegin(&op->strdb,&cur)) >= 0) {
	        while (hdb_enum(&op->strdb,&cur,&key,&val) >= 0) {
	            ip = (int *) val.buf ;
	            rec[c++] = *ip ;
	        } /* end while (looping through strings) */
	        hdb_curend(&op->strdb,&cur) ;
	    } /* end if */
	    rec[c] = -1 ;
	} else {
	    rs = SR_OVERFLOW ;
	}

	return (rs >= 0) ? c : rs ;
}
/* end subroutine (strtab_recmk) */


