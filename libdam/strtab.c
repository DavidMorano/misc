/* store a string into the table */
static int strtab_stuff(STRTAB *op,cchar *sp,int sl)
{
	int		rs ;
	int		amount = (sl + 1) ;
	int		vi = 0 ;

/* do we need to extend the table? */

	if ((rs = strtab_extend(op,amount)) >= 0) {
	    const char	*vp = NULL ;
	    vi = op->stsize ;
	    if ((rs = chunk_add(op->ccp,sp,sl,&vp)) >= 0) {
		int	*ip ;
	        if ((rs = STRTAB_AOGET(&op->intdb,&ip)) >= 0) {
		    HDB_DATUM	key, val ;
	            *ip = vi ;
	            key.buf = vp ;
	            key.len = sl ;
	            val.buf = ip ;
	            val.len = sizeof(int) ;
	            if ((rs = hdb_store(&op->strdb,key,val)) >= 0) {
	                op->stsize += amount ;
	                op->count += 1 ;
	            }
	        } /* end if (AOGET) */
	    } /* end if (chunk-add) */
	} /* end if (extend) */

#if	CF_DEBUGS
	debugprintf("strtab_stuff: ret rs=%d val=%u\n",rs,val) ;
#endif

	return (rs >= 0) ? vi : rs ;
}
/* end subroutine (strtab_stuff) */


/* free up all of the chunks */
static int strtab_finishchunks(STRTAB *op)
{
	VECHAND		*clp = &op->chunks ;
	STRTAB_CHUNK	*ccp ;
	int		rs = SR_OK ;
	int		rs1 ;
	int		i ;
	int		c = 0 ;

	for (i = 0 ; vechand_get(clp,i,&ccp) >= 0 ; i += 1) {
	    if (ccp != NULL) {
	        c += 1 ;
	        rs1 = chunk_finish(ccp) ;
	        if (rs >= 0) rs = rs1 ;
	        rs1 = uc_free(ccp) ;
	        if (rs >= 0) rs = rs1 ;
	    }
	} /* end for */

#if	CF_DEBUGS
	debugprintf("strtab_finishchunks: cs=%u chunks_freed=%u\n",
	    op->chunksize,c) ;
#endif

	op->ccp = NULL ;
	return (rs >= 0) ? c : rs ;
}
/* end subroutine (strtab_finishchunks) */


/* extend the string table */
static int strtab_extend(STRTAB *op,int amount)
{
	int		rs = SR_OK ;

	if (op->ccp != NULL) {
	    const int	rso = SR_OVERFLOW ;
	    if ((rs = chunk_check(op->ccp,amount)) == rso) {
		rs = SR_OK ;
	        op->ccp = NULL ;
	    }
	} /* end if (tried to extend a chunk) */

	if ((rs >= 0) && (op->ccp == NULL)) {
	    rs = strtab_newchunk(op,amount) ;
	}

	return rs ;
}
/* end subroutine (strtab_extend) */


static int strtab_newchunk(STRTAB *op,int amount)
{
	const int	size = sizeof(STRTAB_CHUNK) ;
	int		rs ;
	int		start = 0 ;

#if	CF_DEBUGS
	debugprintf("strtab_newchunk: ent\n") ;
#endif

	op->ccp = NULL ;
	if ((rs = uc_malloc(size,&op->ccp)) >= 0) {
	    if (op->stsize == 0) {
	        op->stsize = 1 ;
	        start = 1 ;
	    }
	    if (amount < op->chunksize) amount = op->chunksize ;
	    if ((rs = chunk_start(op->ccp,amount,start)) >= 0) {
		rs = vechand_add(&op->chunks,op->ccp) ;
		if (rs < 0)
		    chunk_finish(op->ccp) ;
	    }
	    if (rs < 0) {
	        uc_free(op->ccp) ;
	        op->ccp = NULL ;
	    }
	} /* end if (memory-allocation) */

	return rs ;
}
/* end subroutine (strtab_newchunk) */


static int chunk_start(STRTAB_CHUNK *ccp,int chunksize,int start)
{
	int		rs = SR_OK ;

#if	CF_DEBUGS
	debugprintf("chunk_start: start=%u\n",start) ;
#endif

	if (chunksize < STRTAB_MINCHUNKSIZE) chunksize = STRTAB_MINCHUNKSIZE ;

	memset(ccp,0,sizeof(STRTAB_CHUNK)) ;
	if (start > chunksize) chunksize = start ;

	if ((rs = uc_malloc(chunksize,&ccp->cdata)) >= 0) {
	    ccp->csize = chunksize ;
	    ccp->cdata[0] = '\0' ;
	    ccp->cl = start ;
	}

	return rs ;
}
/* end subroutine (chunk_start) */


static int chunk_finish(STRTAB_CHUNK *ccp)
{
	int		rs = SR_OK ;
	int		rs1 ;

	if (ccp->cdata != NULL) {
	    rs1 = uc_free(ccp->cdata) ;
	    if (rs >= 0) rs = rs1 ;
	    ccp->cdata = NULL ;
	}

	ccp->csize = 0 ;
	ccp->cl = 0 ;
	return rs ;
}
/* end subroutine (chunk_finish) */


static int chunk_check(STRTAB_CHUNK *ccp,int amount)
{
	int		rs = SR_OK ;

	if (amount > (ccp->csize - ccp->cl)) {
	    rs = SR_OVERFLOW ;
	}

	return rs ;
}
/* end subroutine (chunk_check) */


static int chunk_add(STRTAB_CHUNK *ccp,cchar *sp,int sl,cchar **spp)
{
	int		rs = SR_OK ;
	int		amount = (sl + 1) ;

#if	CF_DEBUGS
	debugprintf("strtab/chunk_add: tl=%u s=%t\n",ccp->cl,sp,sl) ;
#endif

	if (amount <= (ccp->csize - ccp->cl)) {
	    char	*bp = (ccp->cdata + ccp->cl) ;
	    *spp = bp ;
	    strwcpy(bp,sp,sl) ;
	    ccp->cl += amount ;
	    ccp->count += 1 ;
	} else {
	    *spp = NULL ;
	    rs = SR_NOANODE ;
	}

	return rs ;
}
/* end subroutine (chunk_add) */


static int indexlen(int n)
{
	return nextpowtwo(n) ;
}
/* end subroutine (indexlen) */


static int indexsize(int il)
{
	return ((il + 1) * 3 * sizeof(int)) ;
}
/* end subroutine (indexsize) */


/* calculate the next hash from a given one */
static int hashindex(uint hv,int n)
{
	int		hi = MODP2(hv,n) ;
	if (hi == 0) hi = 1 ;
	return hi ;
}
/* end subroutine (hashindex) */


